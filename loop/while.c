// C program to illustrate 
// while loop
#include <stdio.h>

// Driver code
int main()
{
// Initialization expression
int i = 10; 

// Test expression
while(i <= 10) //true
{
	// loop body
	printf("%d\n",i); 
	
	// update expression
    i++;
} 
while(i >= 1) //true
{
	// loop body
	printf("%d\n",i); 
	
	// update expression
    i++;
} 

return 0;
}
